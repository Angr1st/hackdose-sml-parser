macro_rules! generate_obis {

     ($( ($x:ident, $y:expr, $l:literal) ),*) => {
        #[non_exhaustive]
        pub enum Obis {
             $(
                #[doc = $l]
                 $x,
             )*
         }

        impl Obis {
             pub fn obis_number(&self) -> &[u8] {
                 match self {
                    $(
                        Self:: $x => $y,
                    )*
                 }
             }
         }
    };
}

